function doceava(x, y, x1, y1, x2, y2, x3, y3, x4, y4, x5, y5, x6, y6, x7, y7, x8, y8, x9, y9, x10, y10, x11, y11, x12, y12)
    A = [x^12 x^11 x^10 x^9 x^8 x^7 x^6 x^5 x^4 x^3 x^2 x 1; 
         x1^12 x1^11 x1^10 x1^9 x1^8 x1^7 x1^6 x1^5 x1^4 x1^3 x1^2 x1 1;
         x2^12 x2^11 x2^10 x2^9 x2^8 x2^7 x2^6 x2^5 x2^4 x2^3 x2^2 x2 1;
         x3^12 x3^11 x3^10 x3^9 x3^8 x3^7 x3^6 x3^5 x3^4 x3^3 x3^2 x3 1;
         x4^12 x4^11 x4^10 x4^9 x4^8 x4^7 x4^6 x4^5 x4^4 x4^3 x4^2 x4 1;
         x5^12 x5^11 x5^10 x5^9 x5^8 x5^7 x5^6 x5^5 x5^4 x5^3 x5^2 x5 1;
         x6^12 x6^11 x6^10 x6^9 x6^8 x6^7 x6^6 x6^5 x6^4 x6^3 x6^2 x6 1;
         x7^12 x7^11 x7^10 x7^9 x7^8 x7^7 x7^6 x7^5 x7^4 x7^3 x7^2 x7 1;
         x8^12 x8^11 x8^10 x8^9 x8^8 x8^7 x8^6 x8^5 x8^4 x8^3 x8^2 x8 1;
         x9^12 x9^11 x9^10 x9^9 x9^8 x9^7 x9^6 x9^5 x9^4 x9^3 x9^2 x9 1;
         x10^12 x10^11 x10^10 x10^9 x10^8 x10^7 x10^6 x10^5 x10^4 x10^3 x10^2 x10 1;
         x11^12 x11^11 x11^10 x11^9 x11^8 x11^7 x11^6 x11^5 x11^4 x11^3 x11^2 x11 1;
         x12^12 x12^11 x12^10 x12^9 x12^8 x12^7 x12^6 x12^5 x12^4 x12^3 x12^2 x12 1];
    b = [y; y1; y2; y3; y4; y5; y6; y7; y8; y9; y10; y11; y12];
    coef = A \ b;
    a = coef(1);
    b = coef(2);
    c = coef(3);
    d = coef(4);
    e = coef(5);
    f = coef(6);
    g = coef(7);
    h = coef(8);
    i = coef(9);
    j = coef(10);
    k = coef(11);
    l = coef(12);
    x_reg = linspace(min([x x1 x2 x3 x4 x5 x6 x7 x8 x9 x10 x11 x12]), max([x x1 x2 x3 x4 x5 x6 x7 x8 x9 x10 x11 x12]), 100);
    y_reg = a*x_reg.^12 + b*x_reg.^11 + c*x_reg.^10 + d*x_reg.^9 + e*x_reg.^8 + f*x_reg.^7 + g*x_reg.^6 + h*x_reg.^5 + i*x_reg.^4 + j*x_reg.^3 + k*x_reg.^2 + l*x_reg + coef(13);
    hold on;
    plot([x x1 x2 x3 x4 x5 x6 x7 x8 x9 x10 x11 x12], [y y1 y2 y3 y4 y5 y6 y7 y8 y9 y10 y11 y12], 'o');
    plot(x_reg, y_reg);
end
